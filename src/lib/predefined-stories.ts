// Check if a predefined story exists for a set of words
export function getPredefinedStory(words: { kr: string }[], lang: 'zh' | 'en'): { title: string; story: string } | null {
    const key = words.map(w => w.kr.trim()).sort().join(',');
    const storyData = PREDEFINED_STORIES[key];
    if (!storyData) return null;

    return {
        title: lang === 'zh' ? storyData.titleZh : storyData.titleEn,
        story: lang === 'zh' ? storyData.storyZh : storyData.storyEn
    };
}
